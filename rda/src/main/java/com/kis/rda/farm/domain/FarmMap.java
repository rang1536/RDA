package com.kis.rda.farm.domain;

public class FarmMap {
	private int farmNo;
	private String farmName;
	private String farmCate;
	private String farmEntity;
	private String farmCeo;
	private String farmPhone;
	private String farmDo;
	private String farmCity;
	private String farmDong;
	private String farmSangseAdd;
	private String farmLat;
	private String farmLng;
	private String farmFullAdd;
	public int getFarmNo() {
		return farmNo;
	}
	public void setFarmNo(int farmNo) {
		this.farmNo = farmNo;
	}
	public String getFarmName() {
		return farmName;
	}
	public void setFarmName(String farmName) {
		this.farmName = farmName;
	}
	public String getFarmCate() {
		return farmCate;
	}
	public void setFarmCate(String farmCate) {
		this.farmCate = farmCate;
	}
	public String getFarmEntity() {
		return farmEntity;
	}
	public void setFarmEntity(String farmEntity) {
		this.farmEntity = farmEntity;
	}
	public String getFarmCeo() {
		return farmCeo;
	}
	public void setFarmCeo(String farmCeo) {
		this.farmCeo = farmCeo;
	}
	public String getFarmPhone() {
		return farmPhone;
	}
	public void setFarmPhone(String farmPhone) {
		this.farmPhone = farmPhone;
	}
	public String getFarmDo() {
		return farmDo;
	}
	public void setFarmDo(String farmDo) {
		this.farmDo = farmDo;
	}
	public String getFarmCity() {
		return farmCity;
	}
	public void setFarmCity(String farmCity) {
		this.farmCity = farmCity;
	}
	public String getFarmDong() {
		return farmDong;
	}
	public void setFarmDong(String farmDong) {
		this.farmDong = farmDong;
	}
	public String getFarmSangseAdd() {
		return farmSangseAdd;
	}
	public void setFarmSangseAdd(String farmSangseAdd) {
		this.farmSangseAdd = farmSangseAdd;
	}
	public String getFarmLat() {
		return farmLat;
	}
	public void setFarmLat(String farmLat) {
		this.farmLat = farmLat;
	}
	public String getFarmLng() {
		return farmLng;
	}
	public void setFarmLng(String farmLng) {
		this.farmLng = farmLng;
	}
	public String getFarmFullAdd() {
		return farmFullAdd;
	}
	public void setFarmFullAdd(String farmFullAdd) {
		this.farmFullAdd = farmFullAdd;
	}
	@Override
	public String toString() {
		return "FarmMap [farmNo=" + farmNo + ", farmName=" + farmName + ", farmCate=" + farmCate + ", farmEntity="
				+ farmEntity + ", farmCeo=" + farmCeo + ", farmPhone=" + farmPhone + ", farmDo=" + farmDo
				+ ", farmCity=" + farmCity + ", farmDong=" + farmDong + ", farmSangseAdd=" + farmSangseAdd
				+ ", farmLat=" + farmLat + ", farmLng=" + farmLng + ", farmFullAdd=" + farmFullAdd + "]";
	}
	
	
}
